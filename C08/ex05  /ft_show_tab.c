#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putnbr(int n)
{
	char	a;

	if (n >= 10)
	{
		ft_putnbr(n / 10);
		n %= 10;
	}
	if (0 <= n && n < 10)
	{
		a = n + '0';
		write(1, &a, 1);
	}
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str)
	{
		if (par[i].size < 0)
			return ;
		write(1, par[i].str, par[i].size);
		write(1, "\n", 1);
		ft_putnbr(par[i].size);
		write(1, "\n", 1);
		write(1, par[i].copy, par[i].size);
		write(1, "\n", 1);
		i++;
	}
}
